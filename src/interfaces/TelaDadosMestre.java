    private void salvar_grupo_btActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvar_grupo_btActionPerformed
        if ( (!descricao_grupo_jtf.getText().trim().equals("")) 
          && (!cor_R_grupo_jftf.getText().trim().equals(""))
          && (!cor_G_grupo_jftf.getText().trim().equals(""))
          && (!cor_B_grupo_jftf.getText().trim().equals(""))){
            int corR = Integer.parseInt(cor_R_grupo_jftf.getText());
            int corG = Integer.parseInt(cor_G_grupo_jftf.getText());
            int corB = Integer.parseInt(cor_B_grupo_jftf.getText()); 
            if(corR > 255){
                corR = 255;
            }
            if(corG > 255){
                corG = 255;
            }
            if(corB > 255){
                corB = 255; 
            }
            int cod = 0;
            if(!cod_grupo_jftf.getText().trim().equals("")){
                cod = Integer.parseInt(cod_grupo_jftf.getText());
            }
            
            DBGrupo grupo = new DBGrupo(cod, descricao_grupo_jtf.getText().trim(), corR, corG, corB);
            try {
                if (cod == 0) {
                    DAOPhotoFlow.insertGrupo(con, grupo);
                }else{
                    DAOPhotoFlow.updateGrupo(con, grupo);
                }
                                  
               //carrega lista atualizada
               load_lista_grupos_jT();
               limparTelaGrupo();
            } catch (SQLException ex) {
                Logger.getLogger(TelaConfiguracoes.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            if(descricao_grupo_jtf.getText().trim().equals("")){
                descricao_grupo_jtf.requestFocus();
            }else{
                if (cor_R_grupo_jftf.getText().trim().equals("")) {
                    cor_R_grupo_jftf.requestFocus();
                }else{
                    if (cor_G_grupo_jftf.getText().trim().equals("")) {
                        cor_G_grupo_jftf.requestFocus();
                    }else{
                        if(cor_B_grupo_jftf.getText().trim().equals("")){
                            cor_B_grupo_jftf.requestFocus();
                        }
                    }
                }
            }   
        } 
    }//GEN-LAST:event_salvar_grupo_btActionPerformed

