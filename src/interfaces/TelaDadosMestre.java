    private void salvar_tpAgendamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvar_tpAgendamentoActionPerformed
        if ( (!descricao_tpAgendamento_jtf.getText().trim().equals("")) 
          && (!tempo_medio_tpAgendamento_jftf.getText().trim().equals(""))
          && (!margem_segurança_jftf.getText().trim().equals(""))
          && (!hora_inicio_sug_jftf.getText().trim().equals(""))
          && (!hora_fim_sug_jftf.getText().trim().equals(""))
          && (!justificativa_tpAgendamento_jtf.getText().trim().equals(""))
          && (!grupo_tpAgendamento.getText().trim().equals(""))){
            
            DBTipo_Agendamento tpAgendamento = new DBTipo_Agendamento(getCod_tpAgendamento(),
                                              descricao_tpAgendamento_jtf.getText(), tempo_medio_tpAgendamento_jftf.getText(),
                                              Double.parseDouble(margem_segurança_jftf.getText()), 
                                              hora_inicio_sug_jftf.getText(), hora_fim_sug_jftf.getText(),
                                              justificativa_tpAgendamento_jtf.getText(), getCod_grupo(), descricao_grupo_jtf.getText());
            
            
            try {
                if (getCod_tpAgendamento() == 0) {
                    DAOPhotoFlow.insertTipoAgendamento(con, tpAgendamento);
                }else{
                    DAOPhotoFlow.updateTipoAgendamento(con, tpAgendamento);
                }
                
                                  
               //carrega lista atualizada
               load_lista_tpAgendamento_jT();
               limparTelaTpAgendamento();
            } catch (SQLException ex) {
                Logger.getLogger(TelaConfiguracoes.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            if(descricao_tpAgendamento_jtf.getText().trim().equals("")){
                descricao_tpAgendamento_jtf.requestFocus();
            }else{
                if (tempo_medio_tpAgendamento_jftf.getText().trim().equals("")) {
                    tempo_medio_tpAgendamento_jftf.requestFocus();
                }else{
                    if (margem_segurança_jftf.getText().trim().equals("")) {
                        margem_segurança_jftf.requestFocus();
                    }else{
                        if (hora_inicio_sug_jftf.getText().trim().equals("")) {
                            hora_inicio_sug_jftf.requestFocus();
                        }else{
                            if (hora_fim_sug_jftf.getText().trim().equals("")) {
                                hora_fim_sug_jftf.requestFocus();
                            }else{
                                if (justificativa_tpAgendamento_jtf.getText().trim().equals("")) {
                                    justificativa_tpAgendamento_jtf.requestFocus();
                                }else{
                                    if (grupo_tpAgendamento.getText().trim().equals("")) {
                                        grupo_tpAgendamento.requestFocus();
                                    }
                                }
                            }
                        }
                    }
                }
            }   
        } 
    }//GEN-LAST:event_salvar_tpAgendamentoActionPerformed

